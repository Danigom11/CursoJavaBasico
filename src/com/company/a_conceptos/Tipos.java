        double decimal2 = 9.99d;

        // caracter solo uno
        char caracter1 = 'a';

        // booleanos
        boolean verdadero = true;
        boolean falso = false;

        // TIPO CLASE
        // cadenas de texto
        String nombre = "Dani";
        String apellido = "Sastre";

        // TIPO ENVOLTORIO
        // permite valor null
        // un dato que a√∫n no se conoce
