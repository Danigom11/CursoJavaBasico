package com.company.aconceptos;

public class ForLoop {
    public static void main(String[] args) {

        // Primero se declara la variable para iterar
        // Se suele usar i, pero pueden ser j, l, n...
        // Después condición de parada
        // Por último el incremento
        // Se usan principalmente con arrays.
        // Se pueden combinar diferentes tipos de estructuras de control

        for (int i = 0; i <= 10; i++) {
            System.out.println(i + " Hola mundo");
        }
        System.out.println("Fin");
    }
}